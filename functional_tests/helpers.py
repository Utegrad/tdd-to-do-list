import time

from selenium.common.exceptions import WebDriverException

MAX_WAIT = 5


def wait(fn):
    start_time = time.time()
    while True:
        try:
            return fn()
        except (AssertionError, WebDriverException) as e:
            if time.time() - start_time > MAX_WAIT:
                raise e
            time.sleep(0.5)

